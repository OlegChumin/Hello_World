package materials.lesson_02_05.if_else;

public class IfElseExample_IF_ELSE {
    public static void main(String[] args) {
        int number = -5;

        if (number > 0) {
            System.out.println("Число положительное");
        } else {
            System.out.println("Число отрицательное или равно нулю");
        }
    }
}
